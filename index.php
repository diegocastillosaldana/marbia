            <h2>Our process</h2>
            <div class="container_text_about">
                <p>
                    1.- Design consultation: The first step to transforming your home will
                     be scheduling a free design consultation, where you will meet with one 
                     of our design consultants and go over the scope of work, helping us better
                     understand your vision. Once we have a good understanding of your wants and needs,
                     we will walk the property to assess, go over samples for different products.
                </p>
                <p>
                    2.- Planning and Estimate: Next step will be receiving an estimate, detailing 
                    the scope of work and cost for each part of the project. 
                </p>
                <p>
                    3.- 3D design: Once the estimate is approved, we will schedule a meeting with
                     our Marbia design team to go over scope. When the first rendering is drafted, 
                     we meet again for corrections. Once we have our final design approved, we can 
                     start the work!
                </p>
                <p>
                    4.- Pre-walk: Before we are ready to break ground, we will have a pre walk with
                    our project manager, to go over scope one last time, coordinate material placement,
                     deliveries, and finalize details. We aim to make the process as smooth and enjoyable
                      as possible for our customers, we believe communication is a major key to success. 
                </p>
                <p>
                    5.- Build: Now It is time for the fun stuff...Remodeling! We will work hard to make 
                    your vision a beautiful, functional and enduring reality. All our team members and strive
                     for perfection with each project as if it were our own.
    
